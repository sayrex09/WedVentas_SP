package com.sayrex.model;

public class Tag {
    private int idTag;
    private String nombreTag;

    //setters and getters
    public int getIdTag() {
        return idTag;
    }
    public void setIdTag(int idTag) {
        this.idTag = idTag;
    }
    public String getNombreTag() {
        return nombreTag;
    }
    public void setNombreTag(String nombreTag) {
        this.nombreTag = nombreTag;
    }
}
